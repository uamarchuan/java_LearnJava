package ua.learn.java;

public class java_increment {
	
	public static void main(String[] args){
		//
		int number1 = 5;
		++number1;		
		System.out.println(number1);
		//
		int number2 = 3;		
		System.out.println(number2++);
		System.out.println(number2);
		//
		int number3 = 10;
		number3 += 2;
		System.out.println(number3);
	}

}
