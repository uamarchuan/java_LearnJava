package ua.learn.java;

public class java_switchStatement {
	
	public static void main(String[] args){
		//
		int test = 3;
		
		switch (test) {
		case 1:
			System.out.println("1");
			break;
		case 2:
			System.out.println("3");
			break;
		case 3:
			System.out.println("3");
			break;
		default:
			System.out.println("Other value");
			break;
		}
	}

}
